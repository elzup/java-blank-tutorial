package basic._01variables;

public class PracticeInt {
  public static void main(String[] args) {
    int[] k = new int[10];
    System.out.println(k.length());
    System.out.println(k.length);

    String s = "";
    System.out.println(s.length());
    System.out.println(s.length);

    char[] cs = new char[10];
    System.out.println(cs.length());
    System.out.println(cs.length);
  }
}
